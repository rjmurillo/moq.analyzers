namespace PerfDiff.BDN.DataContracts;

/// <summary>
/// Represents percentile values for a data set.
/// </summary>
public class Percentiles
{
    /// <summary>
    /// Gets or sets the 0th percentile value.
    /// </summary>
    public double P0 { get; set; }

    /// <summary>
    /// Gets or sets the 25th percentile value.
    /// </summary>
    public double P25 { get; set; }

    /// <summary>
    /// Gets or sets the 50th percentile value.
    /// </summary>
    public double P50 { get; set; }

    /// <summary>
    /// Gets or sets the 67th percentile value.
    /// </summary>
    public double P67 { get; set; }

    /// <summary>
    /// Gets or sets the 80th percentile value.
    /// </summary>
    public double P80 { get; set; }

    /// <summary>
    /// Gets or sets the 85th percentile value.
    /// </summary>
    public double P85 { get; set; }

    /// <summary>
    /// Gets or sets the 90th percentile value.
    /// </summary>
    public double P90 { get; set; }

    /// <summary>
    /// Gets or sets the 95th percentile value.
    /// </summary>
    public double P95 { get; set; }

    /// <summary>
    /// Gets or sets the 100th percentile value.
    /// </summary>
    public double P100 { get; set; }
}
